package condetionstatements;

public class Switch {
	
	public static void main(String ar[]) {
		
		int token =100;
		
		switch (token) {
		
		case 100:
		
		System.out.println("Play jingle bell");
		break;
		
		case 80: 
		
		System.out.println("Play twinkle twinkle");
		
		
		case 60: 
		
		System.out.println("Play little star");
		
		default:
			System.out.println("invalid token value");
		}
			
	}

}
